import yaml
from pyair2stream.io import read_calibration, read_Tseries
from pyair2stream.model import detect_segments, aggregation, statis, call_model
from pyair2stream.optimization import DE_MCMC_mode, forward_mode
from pyair2stream.post_processing import post_process
import numpy as np

def run_calibration():
    import json
    import os
    print("--- 1. Running DE-MCMC Calibration ---")
    data = read_calibration("examples/forward_prediction_intervals/config_calib.yaml")
    read_Tseries(data, 'c')
    aggregation(data)
    statis(data)

    DE_MCMC_mode(data)

    # We can either read the sigma dynamically generated by the run, or compute it.
    # The sidecar file generated by DE_MCMC_mode will have rho and sigma.
    sidecar_path = "examples/forward_prediction_intervals/MCMC_chain_Alpha_historical_1d_meta.json"

    historical_sigma = 0.8
    historical_rho = 0.0
    if os.path.exists(sidecar_path):
        with open(sidecar_path, 'r') as f:
            sidecar_data = json.load(f)
            historical_sigma = sidecar_data.get('sigma', historical_sigma)
            historical_rho = sidecar_data.get('rho', historical_rho)

    print(f"\nCALIBRATION COMPLETE.")
    print(f"Historical Residual Sigma (Observation Error): {historical_sigma:.3f}")
    print(f"Historical Residual Rho (Autocorrelation): {historical_rho:.3f}\n")

    return data.par_best.tolist(), historical_sigma

def run_forward(best_params, historical_sigma, noise_model):
    print(f"--- 2. Running Probabilistic Forward Projection ({noise_model}) ---")

    with open("examples/forward_prediction_intervals/config_forward.yaml", "r") as f:
        config = yaml.safe_load(f)

    config['parameters_forward'] = [float(p) for p in best_params]
    config['forward_options']['residual_sigma'] = float(historical_sigma)

    # Enable the chosen noise model
    config['uncertainty_options'] = {
        'noise_model': noise_model
    }

    config_filename = f"examples/forward_prediction_intervals/config_forward_{noise_model}.yaml"
    with open(config_filename, "w") as f:
        yaml.dump(config, f)

    data = read_calibration(config_filename)
    read_Tseries(data, 'c')

    if data.gap_tolerant and data.segments is None:
        detect_segments(data)

    forward_mode(data)

    import shutil
    import os
    # Rename output so it isn't overwritten by the next run
    src_csv = f"examples/forward_prediction_intervals/Forward_Prediction_Envelopes_Alpha_future_1d.csv"
    dest_csv = f"examples/forward_prediction_intervals/Forward_Prediction_Envelopes_{noise_model}.csv"

    if os.path.exists(src_csv):
        shutil.move(src_csv, dest_csv)

    print(f"Forward run complete! Envelopes saved to {dest_csv}")

def compare_plots():
    import pandas as pd
    import matplotlib.pyplot as plt

    print("--- 3. Plotting IID vs AR(1) Envelopes ---")

    env_iid = pd.read_csv("examples/forward_prediction_intervals/Forward_Prediction_Envelopes_iid.csv")
    env_ar1 = pd.read_csv("examples/forward_prediction_intervals/Forward_Prediction_Envelopes_ar1.csv")

    fig, axes = plt.subplots(2, 1, figsize=(10, 8), sharex=True, sharey=True)

    # Plot IID
    ax = axes[0]
    ax.fill_between(env_iid.index, env_iid['Twat_mod_p5'], env_iid['Twat_mod_p95'], color='green', alpha=0.3, label='90% PI (IID)')
    ax.plot(env_iid.index, env_iid['Twat_mod_p50'], color='black', label='Median (IID)')
    ax.set_title("Forward Projection - IID Noise")
    ax.set_ylabel("Water Temp (°C)")
    ax.legend(loc='lower left', fontsize='small')

    # Plot AR1
    ax = axes[1]
    ax.fill_between(env_ar1.index, env_ar1['Twat_mod_p5'], env_ar1['Twat_mod_p95'], color='blue', alpha=0.3, label='90% PI (AR1)')
    ax.plot(env_ar1.index, env_ar1['Twat_mod_p50'], color='black', label='Median (AR1)')
    ax.set_title("Forward Projection - AR(1) Noise")
    ax.set_xlabel("Days into Future")
    ax.set_ylabel("Water Temp (°C)")
    ax.legend(loc='lower left', fontsize='small')

    plt.tight_layout()
    plt.savefig("examples/forward_prediction_intervals/comparison_iid_vs_ar1.png", dpi=150)
    print("Saved comparison plot to examples/forward_prediction_intervals/comparison_iid_vs_ar1.png")

if __name__ == "__main__":
    import os
    # Clean old artifacts
    for f in ["config_forward_injected.yaml", "Forward_Prediction_Envelopes_iid.csv", "Forward_Prediction_Envelopes_ar1.csv", "comparison_iid_vs_ar1.png"]:
        path = os.path.join("examples/forward_prediction_intervals", f)
        if os.path.exists(path):
            os.remove(path)

    best_params, historical_sigma = run_calibration()
    run_forward(best_params, historical_sigma, "iid")
    run_forward(best_params, historical_sigma, "ar1")
    compare_plots()
